	<h2 style="text-decoration:underline;">Attendance Application Module</h2>
	<p><b>Description:</b> The Attendance module gives users the ability to add attendance records.
		<br /><br />
		
		<b>Features:</b>
		<ul>
			<li>You can create multiple attendance records for a given date.</li>
			<li>The grid that displays your attendance records will give you a sum total for every event for a given date.</li>
			<li>You can toggle the attendance records from the top toolbar. Selecting toggle will hide the sum of all events.</li>
			<li>You can add or delete attendance records.</li>
			<li>When you have 50 attendance records the grid displaying your records will page them. To access your additional attendance records you can select the next page from the bottom toolbar.</li>
			<li>You can add or delete attendance records.</li>
		</ul>
	</p>
	
	<p><b>Adding a record:</b><br />
	   <ul>
		<li>Select 'Add Attendance Record' from the top toolbar. You will see a window that will give you four options.</li>
		<ol>
			<li>Select Gathering: An example would be Sunday Morning, Sunday Night, Wednesday Night.</li>
			<li>Select Event: - An example for Sunday Morning would be Sunday Morning Service</li>
			<li>Date: The date of the event you are tracking attendance for.</li>
			<li>Attendance Count: This is the head count of those at the event.</li>
		</ol>
		<li>Select 'Submit' and your attendance record will be added to the grid.</li>
	   </ul>
	</p>
	
	<p><b>Troubleshooting:</b><br /><br />
	   <b>Q:</b> I don't see any gatherings listed, why?<br />
	   <b>A:</b> You need to go to the Administrator section on the left and select 'Gatherings'. Inside the Gatherings admin module is where you can add and define a Gathering.<br /><br />
	   
	   <b>Q:</b> I don't see any events for the gathering I selected, why?<br />
	   <b>A:</b> You need to go to the Administrator section on the left and select 'Gatherings'. Inside the Gatherings admin module is where you can add Events to a specific gathering.
	</p>
	   
	   
