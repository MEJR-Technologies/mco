	<h2 style="text-decoration:underline;">Attendance Dashboard Help</h2>
	<p><b>Description:</b> The attendance dashabord portal shows attendance statistics for each defined gathering. 
		<br /><br />
		
		<b>Stats Calculations:</b>
		<ul>
			<li><b>Last Record</b> - The head count for the last occurance of this service.</li>
			<li><b>YTD Total</b> - The total head count for the current year.</li>		
			<li><b>YTD Average</b> - The YTD Total divided by the numer of times the service could occur.</li>
			<li><b>Current Month Average</b> - The average for this service for the current month.</li>
			<li><b>YTD Monthly Average</b> - The YTD Total divided by the number of months that have elapsed since the year began.</li>
		</ul>
