	<h2 style="text-decoration:underline;">Events Admin Module</h2>
	<p><b>Description:</b> The Events Admin module gives users the ability to add events that will be used when adding attendance and offering records.
		<br /><br />
		
		<b>Features:</b>
		<ul>
			<li>You can create multiple event records.</li>
			<li>The grid that displays your event records will give you details of that event in the grid columns.</li>		
			<li>You can add, edit or delete event records.</li>
			<li>When you have 50 event records the grid displaying your records will page them. To access your additional event records you can select the next page from the bottom toolbar.</li>
			<li>You can sepcify if the event will track offering or attendance data.</li>
		</ul>
	</p>
	
	<p><b>Adding an Event record:</b><br />
	   <ul>
		<li>Select 'Add Event' from the top toolbar. You will see a window that will give you seven options.</li>
		<ol>
			<li>Name: The name of the event. Example would be Sunday Morning Service.</li>
			<li>Description: A description of the event.</li>
			<li>Attendance Data: Select this if you plan to track attendance data for this event.</li>
			<li>Offering Data: Select this if you plan to track offering data for this event.</li>
			<li>Teacher: The teacher of this event as applicable.</li>
			<li>Event Subject: This is the subject being taught.</li>
			<li>Event Type: Select what event type this event belongs to. An Event Type is a collection of events.</li>
		</ol>
		<li>Select 'Submit' and your event record will be added to the grid.</li>
	   </ul>
	</p>
	
	<p><b>Editing an Event:</b><br />
		   <ul>	
				<li>Select an event you want to edit, then click 'Edit Event' from the top toolbar.</li>
				<li>The form will come up with the event's information. From here you can edit what you need to.</li>
				<li>Click Submit and you are done.</li>
			</ul>
	</p>
	
	<p><b>Troubleshooting:</b><br /><br />
	   <b>Q:</b> I can't see any event types in the selection box, why not?<br />
	   <b>A:</b> You need to go to the Event Types Admin module and add the Event Types there. Once you have added some event types you can add events to them in the Events Admin module.<br /><br />
	</p>
	   
	   
