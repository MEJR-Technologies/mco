	<h2 style="text-decoration:underline;">Event Types Admin Module</h2>
	<p><b>Description:</b> The Event Types Admin module gives users the ability to add event types that will be used to add events to. Event Types will help keep your events organized.
		<br /><br />
		
		<b>Features:</b>
		<ul>
			<li>You can create multiple event type records.</li>
			<li>The grid that displays your event type records will give you details of that event type in the grid columns.</li>
			<li>You can add, edit or delete event type records.</li>
			<li>When you have 50 event type records the grid displaying your records will page them. To access your additional event type records you can select the next page from the bottom toolbar.</li>
			<li>You can sepcify a description for each event type so you can keep track of what each event type is used for.</li>
		</ul>
	</p>
	
	<p><b>Adding an Event Type:</b><br />
	   <ul>
		<li>Select 'Add Event Type' from the top toolbar. You will see a window that will give you two options.</li>
		<ol>
			<li>Name: An example event type would be AM Service or PM Service.</li>
			<li>Description: A description of the event type.</li>
		</ol>
		<li>Select 'Submit' and your event type record will be added to the grid.</li>
	   </ul>
	</p>
	
	<p><b>Editing an Event Type:</b><br />
		   <ul>	
				<li>Select the event type you want to edit, then click 'Edit Event Type' from the top toolbar.</li>
				<li>The form will come up with the event type's information. From here you can edit what you need to.</li>
				<li>Click Submit and you are done.</li>
			</ul>
	</p>	   
	   
