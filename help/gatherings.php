	<h2 style="text-decoration:underline;">Gathering Admin Module</h2>
	<p><b>Description:</b> The Gathering Admin module gives users the ability to add Gatherings. Gatherings can be individual church services or they can be special meetings that run all week. Every time there is a gathering of people that you want to track you create a gathering.<br /><br />
	Once a gathering has been created you can create an attendance record for that gathering in the Attendance application module.
		<br /><br />
		
		<b>Features:</b>
		<ul>
			<li>You can create multiple gathering records.</li>
			<li>The grid that displays your gathering records will give you details of that gathering in the grid columns.</li>		
			<li>You can add, edit or delete gathering records.</li>
			<li>When you have 50 gathering records the grid displaying your records will page them. To access your additional gathering records you can select the next page from the bottom toolbar.</li>
			<li>You can sepcify if the gathering will be a recurring gathering such as a weekly service.</li>
			<li>Most importantly you can and will need to specify which events are included in this gathering.</li>
		</ul>
	</p>
	
	<p><b>Adding a Gathering:</b><br />
	   <ul>
		<li>Select 'Add Gathering' from the top toolbar. You will see a window that will give you seven options.</li>
		<ol>
			<li>Name: An example would be Sunday Morning Service.</li>
			<li>Description: A description of the gathering.</li>
			<li>Start Date: The date that this gathering will start.</li>
			<li>End Date: The date that this gathering will end.</li>
			<li>Recurring: Select this if the gathering will be recurring each week.</li>
			<li>Recur Day: Select which day that the gathering will recurr on.</li>
			<li>Events: Select which events belong to this gathering. For example, a gathering titled Sunday Morning could have the following events:
				<ul>
					<li>Sunday Morning Service</li>
					<li>Nursery</li>
					<li>Children's Church</li>
					<li>Junior Church</li>
					<li>Choir</li>
				</ul>
		</ol>
		<li>Select 'Submit' and your gathering record will be added to the grid.</li>
	   </ul>
	</p>
	
	<p><b>Editing a Gathering:</b><br />
		   <ul>	
				<li>Select the gathering you want to edit, then click 'Edit Gathering' from the top toolbar.</li>
				<li>The form will come up with the gathering's information. From here you can edit what you need to.</li>
				<li>Click Submit and you are done.</li>
			</ul>
	</p>
	
	<p><b>Troubleshooting:</b><br /><br />
	   <b>Q:</b> I can't select multiple events, why not?<br />
	   <b>A:</b> To select multiple events hold the 'Ctrl' key on your computer and the select the events you want to add.<br /><br />
	</p>
	   
	   
