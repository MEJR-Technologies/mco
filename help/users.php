	<h2 style="text-decoration:underline;">Users Admin Module</h2>
	<p><b>Description:</b> The Users Admin module gives users the ability to add other users that will access their churches virtual office at My Church Office.
		<br /><br />
		
		<b>Features:</b>
		<ul>
			<li>You can create multiple user records.</li>
			<li>The grid that displays your user records will give you details on what permissions they have and when they last logged in to My Church Office.</li>		
			<li>You can add, edit or delete user records.</li>
			<li>When you have 50 user records the grid displaying your records will page them. To access your additional user records you can select the next page from the bottom toolbar.</li>
			<li>You can sepcify if the user has Administrator permissions. Having someone set as an admin will allow them to add other user accounts.</li>
		</ul>
	</p>
	
	<p><b>Adding a record:</b><br />
	   <ul>
		<li>Select 'Add User' from the top toolbar. You will see a window that will give you eight options.</li>
		<ol>
			<li>Username: An example would be Jack Smith needs an account. A simple username could be jsmith.</li>
			<li>Password: Type an initial password for the user you are creating.</li>
			<li>Confirm Password: Type the password in again so we make sure that you typed it correctly.</li>
			<li>Email Address: The email address of the user you are creating.</li>
			<li>Church Admin?: This is what gives a user Administrative permissions over your entire church account on My Church Office.</li>
			<li>Edit Rights: This will give a user the ability to edit records. Such as attendance records.</li>
			<li>Account Active: This will disable a users account. If you want to create a user, but don't want that user loggin in yet, you can disable it. Also at any time in the future you want to remove access to a given user, you may do so by setting the account to not active.</li>
			<li>Can Upload?: This will give the user the ability to upload documents to the document module.</li>
		</ol>
		<li>Select 'Submit' and your user record will be added to the grid.</li>
	   </ul>
	</p>
	
	<p><b>Editing a record:</b><br />
		   <ul>	
				<li>Select the user you want to edit, then click 'Edit User' from the top toolbar.</li>
				<li>The form will come up with the user's information. From here you can edit what you need to.</li>
				<li>Click Submit and you are done.</li>
			</ul>
	</p>
	
	<p><b>Troubleshooting:</b><br /><br />
	   <b>Q:</b> My user can't edit any records, why not?<br />
	   <b>A:</b> You need to make sure that the user in question has permissions to edit records. In the Users grid you will see a column called Edit Rights. If it says 'No' in this record for the user in question you will have to change that.<br /><br />
	</p>
	   
	   
